package packages;

public class parent
{
    protected int id;
    protected String name;
    protected int age;
    public int get_id()
    {
        return id;
    }
    public void set_id(int num)
    {
        id=num;
    }
    public String get_name()
    {
        return name;
    }
    public void set_name(String str)
    {
        str = name;
    }
    public int get_age()
    {
        return age;
    }
    public void set_age(int num)
    {
        age=num;
    }
}
